package mvcDesignPattern_Employee;

public class Employee {
    private String rolNo;
    private String name;

    public String getRolNo() {
        return rolNo;
    }

    public String getName() {
        return name;
    }

    public void setRolNo(String rolNo) {
        this.rolNo = rolNo;
    }

    public void setName(String name) {
        this.name = name;
    }
}
